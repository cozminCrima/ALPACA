package colliders;

/**
 * Enumerație care conține etichetele pentru collider-e.
 */
public enum ColliderTag {
  Player, // Eticheta pentru jucător
  Enemy, // Eticheta pentru inamic
  CactusEnemy, // Eticheta pentru inamic tip cactus
  Projectile, // Eticheta pentru proiectil
  PlayerProjectile, // Eticheta pentru proiectilul jucătorului
  Wall, // Eticheta pentru perete
  Terrain // Eticheta pentru teren
}
